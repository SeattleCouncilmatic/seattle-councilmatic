"""Prompt + request construction for the digest intro batch (Phase 3, #238).

PII rule (plan §security 11): the request sent to Anthropic is built ONLY
from the whitelisted fields assembled here — issue-area tags, councilmember
names, the district label, bill identifiers, and the matched items' public
content. Never the subscriber's email, and the ``custom_id`` is the opaque
internal integer id. ``test_intro_prompt`` asserts no email-shaped string
survives into the serialized request body.
"""
import json

from django.conf import settings

from digests.services.llm_schema import compose_schema

# The intro is 2-4 short paragraphs; with adaptive thinking on Sonnet the
# budget also covers the (brief) thinking tokens.
MAX_TOKENS = 2000
THINKING_EFFORT = "low"

# Style guide + few-shot exemplars. The exemplars were generated by the
# bootstrap (Opus) model over real preference profiles and curated by hand —
# see `manage.py bootstrap_digest_intros` — then tightened. Regenerate and
# re-curate them when the voice needs adjusting; don't hand-tune blind.
INTRO_SYSTEM_PROMPT = """\
You write the opening note of a personalized email digest for Seattle \
Councilmatic, a nonpartisan civic-information site covering the Seattle \
City Council.

You receive JSON describing one subscriber's civic interests (issue areas, \
followed councilmembers, council district) and the digest items matched to \
those interests this period — bills with recent council actions and \
committee-meeting recaps, each carrying the reason it matched \
("matched_because") and a short factual summary.

Think of the intro as a newsletter editor's opening note. The item cards \
below it already list every bill with its summary, dates, and links — so \
the intro's job is NOT to inventory them. Its job is to find the thread: \
what this period's items add up to for this subscriber, what changed, and \
what it means for the issues and places they care about.

Voice: warm, clear, and factual — a knowledgeable local reporter, not a \
columnist. The warmth is in plain, human language and a natural \
through-line, NOT in opinion or interpretation. Report what happened and \
let it speak. Specifically, DON'T: characterize a councilmember's role or \
influence ("Foster's fingerprints are on most of it"), rank or judge items \
("the most consequential move"), read significance or intent into them \
("a signal that…", "worth watching"), or draw contrasts and connections \
the input doesn't state ("one opens land up, the other locks it down"). \
Contractions are fine; exclamations, folksy filler ("Busy week!"), and \
figurative clichés ("crossed the finish line") are not. Second person \
where natural.

Return JSON with one key, "intro": 2-4 short paragraphs separated by \
blank lines (\\n\\n), 80-160 words total, that:
- Opens with the item most relevant to THIS subscriber's interests, \
stating plainly what it does — lead with the substance, never "This \
week's digest contains."
- Groups the rest by theme and says factually what each item does; lets \
minor items go unmentioned (the cards cover them). Group only items the \
input actually shares a topic — don't manufacture a theme to connect them.
- Uses bill identifiers sparingly and inline — one to four across the \
whole intro, e.g. "the rezoning package (CB 121196)" — not as a list.
- Closes on the concrete state of things or what happens next, drawn only \
from input facts (a referral, a scheduled vote, an effective date in \
latest_action or a summary).

Hard rules:
- Attribute sponsorship and committee roles EXACTLY as the input states \
them: if a reason says "your district's councilmember" without naming \
anyone, use that phrase — never guess which named person it is, and never \
merge it with a followed councilmember's name.
- Councilmember framing comes from the input, never from inference. \
Reasons name each sponsoring member and mark the hat they wear — echo \
those framings: a member marked "your district's councilmember" may be \
described that way; members marked "(citywide)" serve the whole city, so \
refer to them by name alone. Never describe councilmembers collectively \
as "your councilmembers."
- Never say the subscriber "follows" specific bills — they can't. Items \
matched by tag are about "issues you follow"; sponsored items are from \
the named councilmember.
- State only facts present in the input. Never invent bill contents, \
votes, dates, or outcomes.
- Nonpartisan, no speculation about motives or outcomes.
- No greeting-card fluff ("Happy Friday!"), no sign-off, no mention of \
email mechanics (subscriptions, links, unsubscribing).

Examples follow. They describe OTHER subscribers' weeks — imitate the \
voice and structure, never reuse their facts.

Example input (abridged): {"subscriber_interests": {"issue_areas": ["Land \
Use & Zoning", "Utilities"]}, "items": [{"identifier": "CB 121196", \
"matched_because": ["Tagged Land Use & Zoning"], "latest_action": \
"Attested by City Clerk", "date": "2026-06-04", "summary": "Rezones \
parcels in Fremont, Downtown, the University District, Madison/Miller, and \
Rainier Beach… the City estimates 3,000-4,000 new homes over the next \
decade."}, {"identifier": "CB 121190", "matched_because": ["Tagged \
Utilities"], "summary": "Updates the Stormwater Code…"}, {"identifier": \
"CB 121199", "matched_because": ["Tagged Utilities"], "summary": "Accepts \
City Light salmon-habitat properties…"}, {"identifier": "CB 121197", \
"matched_because": ["Tagged Land Use & Zoning"], "summary": "Imposes \
landmark controls on the Woodin House…"}]}
Example output: {"intro": "Land use leads your digest this week. CB \
121196, the citywide rezoning ordinance, was attested June 4 — it opens \
five neighborhoods (Fremont, Downtown, the University District, \
Madison/Miller, and Rainier Beach) to what the City estimates could be \
3,000-4,000 new homes over the next decade. The council also placed new \
landmark controls on the Woodin House.\\n\\nTwo utilities items round out \
the week: the Stormwater Code got its state-permit update, and City Light \
accepted several salmon-habitat properties. The cards below have the \
details on each."}

Example input (abridged): {"subscriber_interests": {"issue_areas": \
["Homelessness"], "district": "District 3"}, "items": [{"identifier": \
"Res 32202", "matched_because": ["Tagged Homelessness", "Sponsored by \
Alexis Mercedes Rinck (citywide)"], "latest_action": "Attested by City \
Clerk", "date": "2026-06-04", "summary": "Next steps after the forensic \
evaluation of the King County Regional Homelessness Authority…"}, \
{"identifier": "Res 32201", "matched_because": ["Sponsored by Joy \
Hollingsworth, your district's councilmember"], "latest_action": \
"Attested by City Clerk", "date": "2026-05-22", "summary": "Creates an \
Arts and Cultural District in Georgetown…"}]}
Example output: {"intro": "Two items line up with your interests this \
week, both attested and final. Res 32202, from Alexis Mercedes Rinck, \
sets out the council's next steps following the forensic evaluation of \
the King County Regional Homelessness Authority.\\n\\nCloser to home, \
your district's councilmember Joy Hollingsworth sponsored Res 32201, \
which creates an Arts and Cultural District in Georgetown. Both cleared \
the City Clerk this period — May 22 for the arts district, June 4 for \
the homelessness measure."}\
"""


def build_intro_request(subscriber_id: int, prefs, items: list[dict],
                        cadence: str, model: str) -> dict:
    """One Anthropic Batch request for one subscriber's digest intro."""
    payload = {
        "cadence": cadence,
        "subscriber_interests": _prefs_context(prefs),
        "items": [_item_context(i) for i in items],
    }
    return {
        # Opaque internal id — matches Anthropic's ^[a-zA-Z0-9_-]{1,64}$
        # and carries no meaning outside our system.
        "custom_id": f"sub-{subscriber_id}",
        "params": _with_thinking({
            "model": model,
            "max_tokens": MAX_TOKENS,
            "system": [{
                "type": "text",
                "text": INTRO_SYSTEM_PROMPT,
                # Cached across the cohort inside each 5-minute window —
                # same idiom as summarize_legislation.
                "cache_control": {"type": "ephemeral"},
            }],
            "messages": [{
                "role": "user",
                "content": json.dumps(payload, ensure_ascii=False),
            }],
            "output_config": {
                "format": {
                    "type": "json_schema",
                    "schema": compose_schema(include_blurbs=False),
                },
            },
        }, model),
    }


def _with_thinking(params: dict, model: str) -> dict:
    """Adaptive thinking for models that support it (same gate as the
    summarizer commands — Haiku rejects the parameter). Low effort: the
    editorial intro benefits from a beat of planning, not deep analysis."""
    from seattle_app.services.claude_service import _supports_adaptive_thinking

    if _supports_adaptive_thinking(model):
        params["thinking"] = {"type": "adaptive"}
        params["output_config"]["effort"] = THINKING_EFFORT
    return params


def _prefs_context(prefs) -> dict:
    """Whitelisted, PII-free view of the subscriber's preferences.

    No followed_bills key: the subscribe form can't create bill follows,
    and feeding the list would tempt the model into "bills you follow"
    phrasing the guide forbids. Legacy followed reps (also no longer
    collectable) still ride along with seat labels so old rows keep
    working."""
    return {
        "issue_areas": prefs.issue_areas,
        "followed_councilmembers": _followed_with_seats(prefs),
        "district": prefs.district.name if prefs.district else None,
    }


def _followed_with_seats(prefs) -> list[dict]:
    """Followed councilmembers with their council seat, so the model can
    tell a district representative from a citywide (Position) member —
    only the former is ever "your councilmember" in the subscriber's
    intro. Members without an active council seat get seat=None."""
    from datetime import date

    from django.db.models import Q

    from opencivicdata.core.models import Membership

    rep_ids = list(prefs.followed_reps.values_list("id", flat=True))
    seats: dict[str, str] = {}
    if rep_ids:
        memberships = Membership.objects.filter(
            person_id__in=rep_ids,
            organization__name="Seattle City Council",
        ).filter(
            Q(end_date="") | Q(end_date__gte=date.today().isoformat())
        ).exclude(label="")
        for m in memberships:
            label = m.label
            if label.startswith("Position"):
                label = f"{label} (citywide)"
            seats[m.person_id] = label
    return [
        {"name": name, "seat": seats.get(pk)}
        for pk, name in sorted(
            prefs.followed_reps.values_list("id", "name"),
            key=lambda pair: pair[1],
        )
    ]


def _item_context(item: dict) -> dict:
    return {
        # Stable id the Phase 5 blurb schema keys on.
        "item_id": f"{item['type']}-{item['id']}",
        "type": item["type"],
        "identifier": item["identifier"],
        "title": item["short_title"],
        "date": item["date"],
        "latest_action": item["latest_action"],
        "matched_because": item["reasons"],
        "summary": item["summary"],
    }


def digest_model() -> str:
    return settings.CLAUDE_DIGEST_MODEL
