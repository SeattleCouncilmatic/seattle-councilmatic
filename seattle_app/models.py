class HistoricLandmark(models.Model):
    """
    A designated Seattle historic landmark. Ingested from SDCI's Landmarks
    FeatureServer (data-seattlecitygis.opendata.arcgis.com) — a point layer
    of ~500 landmarks with coordinates, ordinance numbers, designation
    documents, and photos.

    designation_type / designation_type_name capture the Roman-numeral
    grouping from SMC Ch. 25.32's PDF tables (I Residences, II Buildings,
    etc.) — nullable because the GIS source doesn't expose the grouping.
    Backfill from the PDF if a concrete use case emerges.
    """
    landmark_number = models.IntegerField(
        unique=True,
        null=True,
        blank=True,
        db_index=True,
        help_text="SDCI's stable landmark sequence ID (LANDNO); upsert key. "
                  "Nullable in the DB because Postgres permits multiple NULLs "
                  "under a unique index; the ingestion command enforces presence."
    )
    designation_type = models.CharField(
        max_length=10,
        blank=True,
        db_index=True,
        help_text="Roman numeral category code (I–IX); blank if unknown"
    )
    designation_type_name = models.CharField(
        max_length=60,
        blank=True,
        help_text="Human label for the category, e.g., 'Residences'"
    )
    name = models.CharField(
        max_length=300,
        db_index=True
    )
    address = models.CharField(
        max_length=500,
        blank=True
    )
    original_address = models.CharField(
        max_length=500,
        blank=True,
        help_text="Original address, if the landmark has since been renamed/readdressed"
    )
    designating_ord_number = models.CharField(
        max_length=20,
        blank=True,
        db_index=True,
        help_text="Ordinance that designated this landmark"
    )
    designation_date = models.DateField(
        null=True,
        blank=True,
        help_text="Effective date of designation (EFF_DATE in SDCI)"
    )
    photo_filename = models.CharField(
        max_length=300,
        blank=True,
        help_text="Filename of the landmark photo on Seattle.gov"
    )
    document_filename = models.CharField(
        max_length=300,
        blank=True,
        help_text="Filename of the designation report PDF on Seattle.gov"
    )
    geolocation = gis_models.PointField(
        srid=4326,
        null=True,
        blank=True,
        help_text="Point from SDCI's GIS layer (WGS84)"
    )
    council_district = models.ForeignKey(
        "reps.District",
        null=True,
        blank=True,
        on_delete=models.SET_NULL,
        related_name="historic_landmarks",
        help_text="District resolved by point-in-polygon against geolocation"
    )
    source_url = models.URLField(
        max_length=500,
        blank=True
    )
    scraped_at = models.DateTimeField(
        auto_now=True
    )

    class Meta:
        ordering = ["designation_type", "name"]
        verbose_name = "Historic Landmark"
        verbose_name_plural = "Historic Landmarks"

    def __str__(self):
        return f"{self.name} ({self.designation_type_name})"


class OrdinanceCodification(models.Model):
    """
    A row from the SMC's Table of Ordinances Codified (pp. 4397+ in the
    PDF), mapping one ordinance number to a human-readable description
    of what it did, plus the SMC chapter/section refs extracted from
    that description.
    """
    ordinance_number = models.CharField(
        max_length=20,
        unique=True,
        db_index=True,
        help_text="May be 'I-137' (initiative) or similar, not just numeric"
    )
    description = models.TextField(
        blank=True,
        help_text="Raw description text from the TOC row (e.g., "
                  "'Amends §§ 23.45.010, 23.45.020; adds § 23.45.025')"
    )
    adopted_date = models.DateField(
        null=True,
        blank=True,
        help_text="Adoption date if sourced from elsewhere; the PDF TOC lists only ord numbers"
    )
    codified_sections = models.JSONField(
        default=list,
        help_text="Chapter/section refs extracted from the description, e.g., "
                  "['23.45.010', '23.45.020', '25.32']"
    )
    source_pdf_page = models.IntegerField(
        null=True,
        blank=True,
        help_text="PDF page where this row was extracted"
    )
    source_url = models.URLField(
        max_length=500,
        blank=True
    )
    scraped_at = models.DateTimeField(
        auto_now=True
    )

    class Meta:
        ordering = ["-adopted_date", "ordinance_number"]
        verbose_name = "Ordinance Codification"
        verbose_name_plural = "Ordinance Codifications"

    def __str__(self):
        return f"Ord. {self.ordinance_number}"


class HistoricReviewDistrict(models.Model):
    """
    A Seattle historic or special-review overlay district. Eight of these
    cover specific neighborhoods (Pioneer Square, International District,
    Ballard Avenue, etc.) and impose additional design-review requirements
    on construction and alteration. Ingested from SDCI's 'Historic and
    Special Review Districts' polygon layer.
    """
    object_id = models.IntegerField(
        unique=True,
        db_index=True,
        help_text="SDCI FeatureServer OBJECTID — upsert key"
    )
    overlay_code = models.CharField(
        max_length=10,
        unique=True,
        db_index=True,
        help_text="Short code (e.g., 'ID', 'BD', 'PS')"
    )
    name = models.CharField(
        max_length=200,
        help_text="Full district name (e.g., 'Ballard Avenue Landmark District')"
    )
    purpose = models.TextField(
        blank=True,
        help_text="Purpose/preamble text from SDCI's public description"
    )
    district_type = models.CharField(
        max_length=20,
        db_index=True,
        help_text="'HISTORIC' for landmark districts, 'OVERLAY' for special review"
    )
    smc_chapter = models.CharField(
        max_length=20,
        blank=True,
        db_index=True,
        help_text="SMC chapter that governs this district (e.g., '25.16'), "
                  "the 'Chapter ' prefix stripped for queryability"
    )
    chapter_link = models.URLField(
        max_length=500,
        blank=True,
        help_text="Municode URL to the governing SMC chapter"
    )
    boundary = gis_models.MultiPolygonField(srid=4326)
    source_url = models.URLField(max_length=500, blank=True)
