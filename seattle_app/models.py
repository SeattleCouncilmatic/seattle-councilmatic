class MunicipalCodeSection(models.Model):
    """
    A section of the Seattle Municipal Code (SMC).
    Parsed from the official PDF and stored for reference and summarization.
    """
    title_number = models.CharField(
        max_length=10,
        help_text="SMC title number (e.g., '23' for Land Use Code)"
    )
    chapter_number = models.CharField(
        max_length=20,
        blank=True,
        help_text="Chapter number (e.g., '23.45')"
    )
    section_number = models.CharField(
        max_length=30,
        db_index=True,
        help_text="Full section number (e.g., '23.45.010')"
    )
    title = models.CharField(
        max_length=500,
        help_text="Section title/heading"
    )
    full_text = models.TextField(
        help_text="Full text of this section"
    )
    plain_summary = models.TextField(
        blank=True,
        help_text="Plain-English summary generated by LLM"
    )
    summary_model = models.CharField(
        max_length=50,
        blank=True,
        help_text="Model used to generate summary (e.g., 'claude-3-haiku')"
    )
    summary_generated_at = models.DateTimeField(
        null=True,
        blank=True,
        help_text="When the summary was generated"
    )
    source_pdf_page = models.IntegerField(
        null=True,
        blank=True,
        help_text="Page number in source PDF"
    )
    last_updated = models.DateTimeField(
        auto_now=True,
        help_text="When this section was last updated"
    )
    created_at = models.DateTimeField(
        auto_now_add=True
    )

    class Meta:
        ordering = ['title_number', 'chapter_number', 'section_number']
        unique_together = ['title_number', 'chapter_number', 'section_number']
        verbose_name = "Municipal Code Section"
        verbose_name_plural = "Municipal Code Sections"

    def __str__(self):
        return f"SMC {self.section_number} - {self.title}"


class LegislationSummary(models.Model):
    """
    Plain-English summary and analysis of legislation (bills, ordinances, etc.).
    Links to affected municipal code sections.
    """
    bill = models.OneToOneField(
        Bill,
        on_delete=models.CASCADE,
        related_name='llm_summary',
        help_text="The legislation being summarized"
    )
    summary = models.TextField(
        help_text="Plain-English summary of what this legislation does"
    )
    impact_analysis = models.TextField(
        blank=True,
        help_text="Analysis of how this affects current municipal code"
    )
    affected_sections = models.ManyToManyField(
        MunicipalCodeSection,
        blank=True,
        related_name='affected_by_legislation',
        help_text="Municipal code sections this legislation modifies"
    )
    key_changes = models.JSONField(
        default=list,
        blank=True,
        help_text="Structured list of key changes"
    )
    model_version = models.CharField(
        max_length=50,
        help_text="Model used (e.g., 'claude-sonnet-4-20250514')"
    )
    generated_at = models.DateTimeField(
        auto_now_add=True
    )
    last_regenerated = models.DateTimeField(
        auto_now=True
    )

    class Meta:
        verbose_name = "Legislation Summary"
        verbose_name_plural = "Legislation Summaries"

    def __str__(self):
        return f"Summary: {self.bill.identifier}"


