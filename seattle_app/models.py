class BillText(models.Model):
    """Extracted plain text of a Bill's substantive attachments.

    Seattle bills carry their content in Legistar document attachments
    — typically a "Summary and Fiscal Note" (.docx) plus, after
    enactment, a "Signed Ordinance NNNNN" (.pdf). The OCD/pupa Bill row
    only stores attachment URLs, so this model is the cache of their
    extracted plain text, keyed 1:1 by Bill. The bill summarizer
    reads from `text` instead of re-downloading on every LLM call.

    `source_documents` is the audit trail: which attachments were
    extracted, what category they fell into, what their byte sizes were,
    and any per-document error message. Re-running extraction overwrites
    `text` and refreshes the audit trail.
    """
    bill = models.OneToOneField(
        Bill,
        on_delete=models.CASCADE,
        related_name="extracted_text",
        help_text="The legislation whose attachments produced this text.",
    )
    text = models.TextField(
        blank=True,
        help_text=(
            "Concatenated plain text of all chosen attachments, with "
            "section markers between sources (staff summary first, then "
            "signed canonical text)."
        ),
    )
    source_documents = models.JSONField(
        default=list,
        blank=True,
        help_text=(
            "Per-document audit trail: list of "
            "{note, url, media_type, category, char_count, error}."
        ),
    )
    extracted_at = models.DateTimeField(auto_now_add=True)
    last_regenerated = models.DateTimeField(auto_now=True)

    class Meta:
        verbose_name = "Bill Text"
        verbose_name_plural = "Bill Texts"

    def __str__(self):
        return f"Text: {self.bill.identifier} ({len(self.text):,} chars)"


class EventTranscript(models.Model):
    """Captioned transcript of a council meeting, scraped from Seattle
    Channel's video archive. One row per ``Event`` (typically Full
    Council meetings; the extractor is filterable but defaults to that).

    Seattle Channel publishes auto-captioned SRT files at a
    deterministic URL once a meeting is archived. The SRT is the raw
    source of truth and stays preserved in ``srt_raw`` for future
    re-processing without re-scraping. ``transcript_text`` is the
    flattened plain-text view (entities decoded, timestamps + sequence
    numbers stripped) for cheap LLM consumption.

    ``chapter_markers`` mirrors the agenda-item index on the Seattle
    Channel video page: a list of ``{label, start_seconds}`` dicts
    ordered by time. The summarizer (issue #149 phase 2) uses these
    to chunk the transcript for per-agenda-item summaries.

    Caption quality is auto-captioned (all-caps, proper-noun errors,
    speaker turns marked only by ``>>`` not by name). Quality is
    mitigated at LLM-summary time by passing the meeting's agenda +
    councilmember roster as structured context. ``video_url`` is
    captured so a future Whisper re-transcription step (issue #149
    phase 3, deferred) wouldn't need to re-scrape Seattle Channel."""

    event = models.OneToOneField(
        Event,
        on_delete=models.CASCADE,
        related_name="transcript",
        help_text="The council meeting this transcript belongs to.",
    )
    srt_raw = models.TextField(
        help_text="Raw SRT as downloaded from Seattle Channel.",
    )
    transcript_text = models.TextField(
        help_text=(
            "Flattened plain text of the SRT (timestamps + sequence "
            "numbers stripped, HTML entities decoded). Speaker-turn "
            "markers '>>' preserved for chunkability."
        ),
    )
    chapter_markers = models.JSONField(
        default=list,
        help_text=(
            "Ordered list of {label, start_seconds} dicts from the "
            "Seattle Channel agenda-index UI. Empty when a meeting "
            "had no marked chapters."
        ),
    )
    source_url = models.URLField(
        max_length=500,
        help_text="Seattle Channel URL the SRT was downloaded from.",
    )
    video_url = models.URLField(
        max_length=500,
        blank=True,
        default="",
        help_text=(
            "video.seattle.gov MP4 URL for the meeting. Captured for "
            "future Whisper re-transcription; not used by the v1 "
            "SRT-based summarizer."
        ),
    )
    scraped_at = models.DateTimeField(auto_now=True)
    created_at = models.DateTimeField(auto_now_add=True)

    class Meta:
        verbose_name = "Event Transcript"
        verbose_name_plural = "Event Transcripts"

    def __str__(self):
        n = len(self.transcript_text)
        return f"Transcript for {self.event} ({n:,} chars)"


class EventSummary(models.Model):
    """LLM-generated summary of a council meeting. One row per ``Event``,
    synthesized from the ``EventTranscript.transcript_text`` plus the
    meeting's agenda items and the current councilmember roster as
    structured prompt context (the roster lets the model cross-reference
    garbled auto-caption names against the real people who could plausibly
    have spoken).

    Two-tier shape:
      - ``overview`` is the 2-4 paragraph plain-prose meeting summary
        that the EventDetail card displays by default.
      - ``item_summaries`` is a list of ``{label, start_seconds, summary}``
        dicts, one per agenda-item chapter from the SRT chunker. The
        frontend renders these as collapsible sections under the overview.

    ``stats_snapshot`` records the structured context that was passed to
    the LLM (agenda items, roster, validated chapter list) so a future
    re-generation against the same inputs is reproducible without
    re-aggregating. Re-summarization is idempotent — ``summarize_events``
    UPSERTs by ``event_id``."""

    event = models.OneToOneField(
        Event,
        on_delete=models.CASCADE,
        related_name="llm_summary",
        help_text="The council meeting this summary belongs to.",
    )
    overview = models.TextField(
        help_text="2-4 paragraph plain-prose meeting overview.",
    )
    item_summaries = models.JSONField(
        default=list,
        help_text=(
            "Per-agenda-item summaries: list of "
            "{label, start_seconds, summary} dicts ordered by time."
        ),
    )
    stats_snapshot = models.JSONField(
        default=dict,
        help_text=(
            "Structured prompt context passed to the LLM — agenda items, "
            "councilmember roster, validated chapter list. Reproducibility "
            "audit trail."
        ),
    )
    model_version = models.CharField(
        max_length=100,
        blank=True,
        default="",
        help_text="Claude model that generated the summary.",
    )
    summary_batch_id = models.CharField(
        max_length=64,
        blank=True,
        default="",
        help_text=(
            "Anthropic Message Batches ID this summary came from. "
            "Empty for synchronous one-off retries."
        ),
    )
    generated_at = models.DateTimeField(
        auto_now=True,
        help_text="Last time the summary was (re-)generated.",
    )
    created_at = models.DateTimeField(auto_now_add=True)

    class Meta:
        verbose_name = "Event LLM Summary"
        verbose_name_plural = "Event LLM Summaries"

    def __str__(self):
        return f"Summary for {self.event}"


