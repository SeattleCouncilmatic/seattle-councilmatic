    zone_id = models.IntegerField(
        db_index=True,
        help_text="SDCI ZONEID"
    )
    zoning = models.CharField(
        max_length=50,
        db_index=True,
        help_text="Full designation including modifiers (e.g., 'NC3-65')"
    )
    base_zone = models.ForeignKey(
        "ZoningCode",
        null=True,
        blank=True,
        on_delete=models.SET_NULL,
        related_name="polygons",
        help_text="FK to the base zone legend entry; null if ZONELUT is not in our legend"
    )
    contract = models.CharField(max_length=40, blank=True)
    ordinance_number = models.CharField(
        max_length=20,
        blank=True,
        db_index=True,
        help_text="Ordinance that created/amended this zone"
    )
    effective_date = models.DateField(null=True, blank=True)
    historic = models.CharField(max_length=100, blank=True)
    pedestrian_overlay = models.CharField(max_length=100, blank=True)
    shoreline_overlay = models.CharField(max_length=100, blank=True)
    other_overlay = models.CharField(max_length=100, blank=True)
    lightrail_overlay = models.CharField(max_length=100, blank=True)
    previous_zone_id = models.IntegerField(null=True, blank=True)
    special_area_id = models.CharField(max_length=30, blank=True)
    boundary = gis_models.MultiPolygonField(srid=4326)
    source_url = models.URLField(max_length=500, blank=True)
    ingested_at = models.DateTimeField(auto_now=True)

    class Meta:
        ordering = ["zoning", "object_id"]
        verbose_name = "Zoning Polygon"
        verbose_name_plural = "Zoning Polygons"

    def __str__(self):
        return f"{self.zoning} (#{self.object_id})"


